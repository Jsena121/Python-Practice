def hello(name):
    print ('Hello, ' + name + '!')

hello ('Alice')
hello ('Bob')

#call: the line calls the function; hello ('Alice') calls the hello(name) function
#pass: function call is also known as passing a string value to a function; string 'Alice' is passed to the hello(name) function.
#argument: value is passed to a function in the function call; argument 'Alice' is assigned to local variable name in hello(name) in line 1.
#parameter: variables (name) that have arguments assigned to them but do not exist outside the function
